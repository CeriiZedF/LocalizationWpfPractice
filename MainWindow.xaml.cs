namespace LocalizationWpf
{
    /// <summary>
    /// Логика взаимодействия для MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            var temp = sender as Button;
           
            Thread.CurrentThread.CurrentUICulture = new CultureInfo(temp.Tag.ToString());
            b1.Content = Localization.b1;
            b2.Content = Localization.b2;
            b3.Content = Localization.b3;
        }
    }
}
